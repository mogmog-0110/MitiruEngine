/// @brief 8x8ビットマップフォントデータ
/// @details ASCII 32-126の95文字分のグリフデータを内蔵する。
///          各グリフは8バイト（8行×8ビット）で表現され、
///          MSBが左端ピクセルに対応する。
///
/// @code
/// const auto g = BitmapFont::glyph('A');
/// for (int row = 0; row < 8; ++row)
/// {
///     for (int col = 0; col < 8; ++col)
///     {
///         if (g[row] & (0x80 >> col)) { /* ピクセルON */ }
///     }
